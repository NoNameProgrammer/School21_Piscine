char	rot(char c)
{
	if (c >= 65 && c <= 90)
	{
		c = c - 65;
		c = ((c + 42) % 26);
		c = c + 65;
		return (c);
	}
	else if (c >= 97 && c <= 122)
	{
		c = c - 97;
		c = ((c + 42) % 26);
		c = c + 97;
		return (c);
	}
	return (c);
}

char	*ft_rot42(char *str)
{
	int index;

	index = 0;
	while (str[index] != '\0')
	{
		str[index] = rot(str[index]);
		index++;
	}
	return (str);
}
